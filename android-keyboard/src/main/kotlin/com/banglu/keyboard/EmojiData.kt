    private val allKnownEmojis: Set<String> by lazy {
        val set = HashSet<String>()
        categories.forEach { set.addAll(it.emojis) }
        set.addAll(EmojiKeywords.keywords.keys)
        set
    }

    /** S57: true when [value] is an emoji this build ships — used to drop
     *  stale recent-row entries from older builds (removed sticker texts). */
    fun isKnownEmoji(value: String): Boolean = value in allKnownEmojis

    /**
     * Ranked three-script search. Order: phrase hits, then emoji whose
     * keyword matches exactly, then by prefix, then by substring. Also
     * matches a literally-typed emoji.
     */
    fun search(query: String, limit: Int = 96): List<String> {
        val key = query.trim().lowercase()
        if (key.isEmpty()) return emptyList()

        val exact = linkedSetOf<String>()
        val prefix = linkedSetOf<String>()
        val contains = linkedSetOf<String>()

        for (phrase in BanglaPhrases.allPhrases) {
            val textLower = phrase.text.lowercase()
            when {
                phrase.aliases.any { it.lowercase() == key } || textLower == key -> exact.add(phrase.text)
                phrase.aliases.any { it.lowercase().startsWith(key) } || textLower.startsWith(key) -> prefix.add(phrase.text)
                phrase.aliases.any { it.lowercase().contains(key) } || textLower.contains(key) -> contains.add(phrase.text)
            }
        }

        for ((emoji, words) in EmojiKeywords.keywords) {
            when {
                words.any { it == key } -> exact.add(emoji)
                words.any { it.startsWith(key) } -> prefix.add(emoji)
                key.length >= 3 && words.any { it.contains(key) } -> contains.add(emoji)
            }
        }

        if (exact.isEmpty() && prefix.isEmpty() && contains.isEmpty()) {
            val literal = query.trim()
            if (categories.any { literal in it.emojis }) exact.add(literal)
        }

        return (exact.asSequence() + prefix.asSequence() + contains.asSequence())
            .distinct()
            .take(limit)
            .toList()
    }
